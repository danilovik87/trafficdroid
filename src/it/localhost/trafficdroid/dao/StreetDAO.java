	public static ArrayList<StreetDTO> getAllEnabled(SharedPreferences settings, Resources resources) {
		createAllStreets();
		for (int strtCount = 0; strtCount < streets.size(); strtCount++) {
			for (int znCount = 0; znCount < streets.get(strtCount).getZones().size(); znCount++)
				if (settings.getBoolean(streets.get(strtCount).getKey(), false) || settings.getBoolean(streets.get(strtCount).getZones().get(znCount).getKey(), false))
					streets.get(strtCount).getZones().get(znCount).setName(resources.getString(streets.get(strtCount).getZones().get(znCount).getId()));
				else {
					streets.get(strtCount).getZones().remove(znCount);
					znCount--;
				}
			if (streets.get(strtCount).getZones().size() > 0)
				streets.get(strtCount).setName(resources.getString(streets.get(strtCount).getId()));
			else {
				streets.remove(strtCount);
				strtCount--;
			}
		}
		return streets;
	}

